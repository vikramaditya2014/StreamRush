export const getTrendingVideos = (): Video[] => {
  return [...mockVideos].sort((a, b) => b.views - a.views);
};

export const getVideosByCategory = (category: string): Video[] => {
  if (category === 'All') return mockVideos;
  return mockVideos.filter(video => video.category === category);
};

export const searchVideos = (query: string): Video[] => {
  const lowercaseQuery = query.toLowerCase();
  return mockVideos.filter(video => 
    video.title.toLowerCase().includes(lowercaseQuery) ||
    video.description.toLowerCase().includes(lowercaseQuery) ||
    video.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

